/**
 * return min(a,b)
 * @param a {int|float} first param
 * @param b {int|float} second param
 * @returns {int|float}
 */
function min(a,b) {
    return a <= b ? a : b;
}

/**
 * return max(a,b)
 * @param a {int|float} first param
 * @param b {int|float} second param
 * @returns {int|float}
 */
function max(a,b) {
    return a >= b ? a : b;
}